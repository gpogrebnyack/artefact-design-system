#!/usr/bin/env node
/*
 * Генерирует src/primitives/icon-catalog.generated.ts — ВЕСЬ каталог
 * Phosphor в двух начертаниях (regular + fill), вкомпилированный в пакет.
 *
 * Решение владельца (Mobbin-эксперимент, 2026-07-15): реестр-словарь
 * REGISTRY читался агентами как закрытый набор («нет в реестре — нет
 * иконки»), хотя был задуман как примеры. Лекарство — забрать к себе весь
 * каталог: IconName = полный словарь Phosphor, вопрос «есть ли иконка»
 * исчезает как класс. Веса thin/light/bold/duotone НЕ вкомпилируются —
 * правило «ровно два начертания» (DESIGN.md → Shapes) остаётся зашитым
 * и в данных, и в типе IconAllowedWeight.
 *
 * Источник — @phosphor-icons/core (devDependency): assets/regular/*.svg и
 * assets/fill/*-fill.svg. Из каждого SVG берутся все path d="…" (у части
 * глифов несколько сабпутей — конкатенация корректна: заливка одноцветная).
 *
 * Запуск: npm run generate:icons (вызывается вручную при обновлении
 * @phosphor-icons/core; результат коммитится).
 */
import { readdirSync, readFileSync, writeFileSync } from "node:fs"
import { join, dirname } from "node:path"
import { fileURLToPath } from "node:url"

const root = join(dirname(fileURLToPath(import.meta.url)), "..")
const assets = join(root, "node_modules/@phosphor-icons/core/assets")
const out = join(root, "src/primitives/icon-catalog.generated.ts")

const pathData = (file) => {
  const svg = readFileSync(file, "utf8")
  const ds = [...svg.matchAll(/ d="([^"]+)"/g)].map((m) => m[1])
  if (!ds.length) throw new Error(`no path data in ${file}`)
  return ds.join(" ")
}

const names = readdirSync(join(assets, "regular"))
  .filter((f) => f.endsWith(".svg"))
  .map((f) => f.replace(/\.svg$/, ""))
  .sort()

const entries = names.map((name) => {
  const regular = pathData(join(assets, "regular", `${name}.svg`))
  const fill = pathData(join(assets, "fill", `${name}-fill.svg`))
  return `  "${name}": [${JSON.stringify(regular)}, ${JSON.stringify(fill)}]`
})

const version = JSON.parse(
  readFileSync(join(assets, "../package.json"), "utf8"),
).version

const file = `/* AUTO-GENERATED by scripts/generate-icon-catalog.mjs — не редактировать руками.
 * Phosphor @phosphor-icons/core v${version}, ${names.length} глифов, веса: regular + fill
 * (остальные четыре веса не вкомпилированы намеренно — DESIGN.md → Shapes).
 * viewBox всех глифов: 0 0 256 256. Формат значения: [regular, fill]. */

export type PhosphorIconName =
${names.map((n) => `  | "${n}"`).join("\n")}

/** path data глифов: [regular, fill] */
export const ICON_CATALOG: Record<PhosphorIconName, [string, string]> = {
${entries.join(",\n")},
}
`

writeFileSync(out, file)
console.log(`✓ ${names.length} глифов → src/primitives/icon-catalog.generated.ts (${(file.length / 1024 / 1024).toFixed(1)}MB)`)
